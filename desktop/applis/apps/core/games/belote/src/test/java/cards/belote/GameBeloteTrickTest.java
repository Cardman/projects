        RulesBelote r_ = new RulesBelote();
        r_.getAllowedBids().put(BidBelote.NO_TRUMP, BoolVal.TRUE);
        byte d_ = 2;
        HandBelote last_ = new HandBelote();
        last_.ajouter(CardBelote.HEART_1);
        HandBelote curHand_ = new HandBelote();
