package code.gui.images;

public final class MetaPoint {
    private final int xcoord;
    private final int ycoord;
    public MetaPoint(int _x, int _y) {
        xcoord = _x;
        ycoord = _y;
    }

    public int getXcoord() {
        return xcoord;
    }

    public int getYcoord() {
        return ycoord;
    }
}
