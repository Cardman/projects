package code.formathtml.util;
import code.xml.components.Node;

public final class ReadWriteHtml {

    private Node read;

    private Node write;

    public Node getRead() {
        return read;
    }

    public void setRead(Node _read) {
        read = _read;
    }

    public Node getWrite() {
        return write;
    }

    public void setWrite(Node _write) {
        write = _write;
    }
}
